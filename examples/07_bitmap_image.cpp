// =============================================================================
//  Example: draw your own image (bitmap) on the display
// =============================================================================
//
//  The display expects images as an array of RGB565 pixel colors (the same
//  16-bit color format used everywhere else in this project, e.g. RED,
//  GREEN, ...). One array entry = one pixel color, ordered row by row from
//  top-left to bottom-right.
//
//  How to make your own image array:
//
//  1) Easiest: use an online converter and paste the result below.
//     - https://javl.github.io/image2cpp/  -> upload an image, then set:
//       "Draw mode" = normal, "Output format" = "plain bytes",
//       and pick a 565 color mode if offered (or use its "Arduino GFX"
//       preset). Keep images SMALL (e.g. 16x16 to 64x64) - this microcontroller
//       has no room for large images, and the SPI transfer takes time.
//     - https://lopaka.app (already used for this project's UI) can also
//       export bitmaps for GFX-style libraries.
//
//  2) With Python (Pillow), if you prefer a script over a website:
//       from PIL import Image
//       img = Image.open("myimage.png").convert("RGB").resize((16, 16))
//       pixels = list(img.getdata())
//       values = []
//       for r, g, b in pixels:
//           rgb565 = ((r & 0xF8) << 8) | ((g & 0xFC) << 3) | (b >> 3)
//           values.append(f"0x{rgb565:04X}")
//       print(", ".join(values))
//     Paste the printed values into an array like the one below.
//
// =============================================================================

